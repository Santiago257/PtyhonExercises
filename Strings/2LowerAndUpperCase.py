#Write your full name and display with lowercase and uppercase
name = input("Write your name: ")
lastname = input("Write your lastname: ")

#Show in lowercase
print("------------------lowercase---------------------")
print(name.lower())
print(lastname.lower())

print("------------------uppercase---------------------")
print(name.upper())
print(lastname.upper())

print("------------Only the first letter --------------")
print(name[0])
print(lastname[0])